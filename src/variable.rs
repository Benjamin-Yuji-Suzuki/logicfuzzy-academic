/// Linguistic term of a fuzzy variable.
///
/// A `Term` associates a natural-language label ("low", "medium", "high")
/// with a membership function (`MembershipFn`).
///
/// Equivalent to the scikit-fuzzy assignment:
///   `variable['low'] = fuzz.trimf(variable.universe, [0, 0, 50])`
///
/// # Exemplo
/// ```
/// use fuzzy_mamdani::{Term, MembershipFn};
/// let t = Term::new("baixo", MembershipFn::Trimf([0.0, 0.0, 50.0]));
/// assert_eq!(t.label, "baixo");
/// assert_eq!(t.mf.eval(0.0), 1.0);
/// assert_eq!(t.mf.eval(50.0), 0.0);
/// ```
#[derive(Debug, Clone)]
pub struct Term {
    /// Linguistic label of the term (e.g., "low", "medium", "high").
    pub label: String,
    /// Membership function associated with this term.
    pub mf: MembershipFn,
}

impl Term {
    /// Creates a new term with a label and membership function.
    pub fn new(label: impl Into<String>, mf: MembershipFn) -> Self {
        Self {
            label: label.into(),
            mf,
        }
    }

    /// Evaluates the membership degree of value `x` for this term.
    pub fn eval(&self, x: f64) -> f64 {
        self.mf.eval(x)
    }

    /// Evaluates the membership degree for a full slice of universe points.
    pub fn eval_universe(&self, points: &[f64]) -> Vec<f64> {
        self.mf.eval_universe(points)
    }
}

