/// Fuzzy linguistic variable with a universe of discourse and a collection of terms.
///
/// Combined equivalent of `ctrl.Antecedent` and `ctrl.Consequent` from scikit-fuzzy.
/// `Antecedent` and `Consequent` are semantic type aliases over this struct.
///
/// # Exemplo basico
/// ```
/// use fuzzy_mamdani::{FuzzyVariable, Universe, Term, MembershipFn};
///
/// let mut temp = FuzzyVariable::new("temperatura", Universe::new(0.0, 50.0, 501));
/// temp.add_term(Term::new("fria",   MembershipFn::Trimf([0.0,  0.0, 25.0])));
/// temp.add_term(Term::new("morna",  MembershipFn::Trimf([0.0, 25.0, 50.0])));
/// temp.add_term(Term::new("quente", MembershipFn::Trimf([25.0,50.0, 50.0])));
///
/// assert!((temp.membership_at("morna", 25.0) - 1.0).abs() < 1e-10);
/// assert!(temp.membership_at("fria",  50.0) == 0.0);
/// ```
#[derive(Debug, Clone)]
pub struct FuzzyVariable {
    /// Variable name (e.g., "temperature", "humidity").
    pub name: String,
    /// Universe of discourse for this variable.
    pub universe: Universe,
    /// List of linguistic terms associated with this variable.
    terms: Vec<Term>,
}

impl FuzzyVariable {
    /// Creates an empty fuzzy variable (no terms).
    pub fn new(name: impl Into<String>, universe: Universe) -> Self {
        Self {
            name: name.into(),
            universe,
            terms: Vec::new(),
        }
    }

    /// Adds a linguistic term to the variable.
    /// Panics if a term with the same label already exists.
    pub fn add_term(&mut self, term: Term) {
        assert!(
            !self.terms.iter().any(|t| t.label == term.label),
            "FuzzyVariable '{}': term '{}' already exists",
            self.name,
            term.label
        );
        self.terms.push(term);
    }

    /// Returns a reference to the term by label. Returns `None` if not found.
    pub fn get_term(&self, label: &str) -> Option<&Term> {
        self.terms.iter().find(|t| t.label == label)
    }

    /// Evaluates the membership degree of value `x` for the term `label`.
    /// Panics if the label does not exist in the variable.
    ///
    /// # Exemplo
    /// ```
    /// use fuzzy_mamdani::{FuzzyVariable, Universe, Term, MembershipFn};
    /// let mut v = FuzzyVariable::new("v", Universe::new(0.0, 10.0, 101));
    /// v.add_term(Term::new("alto", MembershipFn::Trimf([5.0, 10.0, 10.0])));
    /// assert_eq!(v.membership_at("alto", 10.0), 1.0);
    /// ```
    pub fn membership_at(&self, label: &str, x: f64) -> f64 {
        match self.get_term(label) {
            Some(term) => term.eval(x),
            None => panic!(
                "FuzzyVariable '{}': term '{}' not found. Available terms: [{}]",
                self.name,
                label,
                self.term_labels().join(", ")
            ),
        }
    }

    /// Returns the membership degrees of `x` across ALL terms.
    /// Returns a `Vec` of (label, degree) in the order terms were added.
    ///
    /// Useful in the fuzzification step: iterates all terms at once.
    pub fn fuzzify(&self, x: f64) -> Vec<(&str, f64)> {
        self.terms
            .iter()
            .map(|t| (t.label.as_str(), t.eval(x)))
            .collect()
    }

    /// Returns the labels of all terms in this variable.
    pub fn term_labels(&self) -> Vec<&str> {
        self.terms.iter().map(|t| t.label.as_str()).collect()
    }

    /// Returns the number of terms.
    pub fn term_count(&self) -> usize {
        self.terms.len()
    }

    /// Returns a reference to the internal slice of terms.
    pub fn terms(&self) -> &[Term] {
        &self.terms
    }

    /// Returns the discretized points of this variable's universe.
    /// Shortcut for `self.universe.points()`.
    pub fn universe_points(&self) -> Vec<f64> {
        self.universe.points()
    }

    /// Evaluates the MF of a term over the entire discretized universe.
    /// Returns a `Vec<f64>` with one degree per universe point.
    ///
    /// Equivalent to: `skfuzzy.trimf(var.universe, params)`
    pub fn term_membership_curve(&self, label: &str) -> Vec<f64> {
        let term = self
            .get_term(label)
            .unwrap_or_else(|| panic!("FuzzyVariable '{}': term '{}' not found", self.name, label));
        let pts = self.universe_points();
        term.eval_universe(&pts)
    }

    /// Generates an SVG string visualizing all membership functions of this variable.
    ///
    /// The SVG is self-contained and can be saved as a `.svg` file or embedded in HTML.
    /// No external dependencies required.
    ///
    /// # Example
    /// ```
    /// use fuzzy_mamdani::{fuzzy_var};
    /// let var = fuzzy_var!("temperature", 0.0, 50.0, 501,
    ///     "cold" => trimf [0.0,  0.0, 25.0],
    ///     "warm" => trimf [0.0, 25.0, 50.0],
    ///     "hot"  => trimf [25.0,50.0, 50.0],
    /// );
    /// let svg = var.to_svg();
    /// assert!(svg.starts_with("<svg"));
    /// std::fs::write("temperature.svg", &svg).ok();
    /// ```
    pub fn to_svg(&self) -> String {
        crate::svg::render_variable_svg(self, None)
    }

    /// Generates an SVG with a vertical marker at the current input `value`.
    ///
    /// Useful for visualizing how a specific crisp input is fuzzified.
    ///
    /// # Example
    /// ```
    /// use fuzzy_mamdani::{fuzzy_var};
    /// let var = fuzzy_var!("temperature", 0.0, 50.0, 501,
    ///     "cold" => trimf [0.0, 0.0, 25.0],
    ///     "hot"  => trimf [25.0,50.0,50.0],
    /// );
    /// let svg = var.to_svg_with_input(10.0);
    /// assert!(svg.contains("stroke-dasharray"));
    /// ```
    pub fn to_svg_with_input(&self, value: f64) -> String {
        crate::svg::render_variable_svg(self, Some(value))
    }
}

// ─────────────────────────────────────────────────────────────────
// Aliases semanticos: Antecedent e Consequent
// ─────────────────────────────────────────────────────────────────

/// Semantic alias for input variables (antecedents).
/// Equivalent to `ctrl.Antecedent` from scikit-fuzzy.
pub type Antecedent = FuzzyVariable;

/// Semantic alias for output variables (consequents).
/// Equivalent to `ctrl.Consequent` from scikit-fuzzy.
pub type Consequent = FuzzyVariable;

