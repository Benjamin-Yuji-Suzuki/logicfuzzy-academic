//! # variable.rs
//!
//! Linguistic fuzzy variables: universe of discourse, terms and variables.
//! Equivalent to `ctrl.Antecedent` / `ctrl.Consequent` from scikit-fuzzy.
//!
//! Usage pipeline:
//!   1. Create a `Universe` with [min, max, resolution]
//!   2. Create `Term`s with `MembershipFn`
//!   3. Group everything into `FuzzyVariable`
//!   4. Use `Antecedent` / `Consequent` as semantic aliases

use crate::membership::MembershipFn;

// ─────────────────────────────────────────────────────────────────
// Universe
// ─────────────────────────────────────────────────────────────────

/// Universe of discourse for a fuzzy variable.
///
/// Defines the numeric range [min, max] and the number of discretization points (resolution).
/// discretizacao (resolution). Quanto maior o resolution, mais precisa
/// a defuzzificacao por centroide — mas mais custoso computacionalmente.
///
/// Equivalent to the numpy array generated by `np.linspace(min, max, resolution)` in scikit-fuzzy.
/// ou np.linspace(min, max, resolution) do scikit-fuzzy.
///
/// # Exemplo
/// ```
/// use fuzzy_mamdani::Universe;
/// let u = Universe::new(0.0, 100.0, 1001);
/// let pts = u.points();
/// assert_eq!(pts.len(), 1001);
/// assert_eq!(pts[0], 0.0);
/// assert_eq!(pts[1000], 100.0);
/// ```
#[derive(Debug, Clone)]
pub struct Universe {
    /// Minimum value of the universe (inclusive).
    pub min: f64,
    /// Maximum value of the universe (inclusive).
    pub max: f64,
    /// Number of discretization points (>= 2).
    pub resolution: usize,
}

impl Universe {
    /// Creates a new universe. Requires `min < max` and `resolution >= 2`.
    pub fn new(min: f64, max: f64, resolution: usize) -> Self {
        assert!(min < max, "Universe: min must be less than max");
        assert!(resolution >= 2, "Universe: resolution must be >= 2");
        Self {
            min,
            max,
            resolution,
        }
    }

    /// Returns the vector of discrete points representing the universe.
    /// Equivalent to `np.linspace(min, max, resolution)`.
    ///
    /// # Exemplo
    /// ```
    /// use fuzzy_mamdani::Universe;
    /// let u = Universe::new(0.0, 10.0, 11);
    /// let pts = u.points();
    /// assert_eq!(pts.len(), 11);
    /// assert!((pts[5] - 5.0).abs() < 1e-10);
    /// ```
    pub fn points(&self) -> Vec<f64> {
        let n = self.resolution;
        (0..n)
            .map(|i| self.min + (self.max - self.min) * (i as f64) / ((n - 1) as f64))
            .collect()
    }

    /// Step size between consecutive points.
    pub fn step(&self) -> f64 {
        (self.max - self.min) / ((self.resolution - 1) as f64)
    }
}

